func TestCompressedDecode(t *testing.T) {
	for _, test := range compressedDecodeTests {
		var buf [2]byte

		bo.PutUint16(buf[:], test.Instr)
		instr, size, err := Decode(buf[:])
		if err != nil {
			if test.Op != Invalid {
				t.Fatalf("failed to decode %04x: %v", test.Instr, err)
			}
		} else {
			if size != 2 {
				t.Errorf("invalid size, expected 2, got %v", size)
			}
			if instr.Rd != test.Rd {
				t.Errorf("instr %04x: invalid rd: got %v, expected %v",
					test.Instr, instr.Rd, test.Rd)
			}
			if instr.Rs1 != test.Rs1 {
				t.Errorf("instr %04x: invalid rs1: got %v, expected %v",
					test.Instr, instr.Rs1, test.Rs1)
			}
			if instr.Rs2 != test.Rs2 {
				t.Errorf("instr %04x: invalid rs2: got %v, expected %v",
					test.Instr, instr.Rs2, test.Rs2)
			}
			if instr.Imm != test.Imm {
				t.Errorf("instr %04x[%016b]: invalid imm: got %v, expected %v",
					test.Instr, test.Instr, instr.Imm, test.Imm)
			}
			if instr.Op != test.Op {
				t.Errorf("instr %04x: invalid op: got %v, expected %v",
					test.Instr, instr.Op, test.Op)
			}
		}
	}
}
